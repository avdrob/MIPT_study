.model tiny                             ; Задание COM-модели
.code                                   ; Начало кода

org 100h                                ; Необходимое смещение на 100 из-за PSP

start:		mov ah, 9		; Функция ДОСа 9 выводит строку 
		mov dx, offset msg      ; Вычисление адреса msg
		int 21h                 ; Прерывание и передача управления ДОСу
		                        ; для того, чтобы ДОС вывел строку

		mov ax, 4c00h           ; Выход и окончательная передача упра-
		int 21h                 ; вления ДОСу.

.data
msg 		db 'Hello$'

end start

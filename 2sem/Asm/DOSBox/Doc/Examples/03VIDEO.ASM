.model tiny

.code
org 100h

start:		mov ax, 4e21h   ; 4е - цвет (жёлтый на красном), 21 - символ 
		mov bx, 0b800h
		mov es, bx
		mov bx, 0
		mov es:[bx], ax ; помещение цветного символа на зкран

		mov ax, 4c00h	; выход
		int 21h
end start

